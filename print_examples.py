# "Hello" "World"
# Concatenating the texts 
# We can use +(plus) sign or we can use ,(coma)
print("Hello" + "World")
# Hello World
# HelloWorld
# It will print without space
# Plus sign will concatenate the string(texts) as is.

print("Hello", "World")
# coma(,) will concatenate the string(texts) and 
# will put a space between them.